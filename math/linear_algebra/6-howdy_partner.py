#!/usr/bin/env python3

"""
6-howdy_partner - Module for concatenating two arrays

This module contains the cat_arrays function which
concatenates two lists
"""


def cat_arrays(arr1, arr2):
    """
    Concatenate two arrays

    Args:
        arr1 (list): The first list of integers or floats
        arr2 (list): The second list of integers or floats

    Returns:
        list: A new list containing the elements of arr1
        followed by the elements of arr2
    """
    return arr1 + arr2
